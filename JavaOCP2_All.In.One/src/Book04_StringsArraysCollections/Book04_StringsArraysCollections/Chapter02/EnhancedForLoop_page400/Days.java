package Book04_StringsArraysCollections.Book04_StringsArraysCollections.Chapter02.EnhancedForLoop_page400;

public class Days {
    public static void main(String[] args) {
        String[] days = {"Sunday", "Monday", "Tuesday",
                "Wednesday", "Thursday",
                "Friday", "Saturday"};
        for (String day : days) {
            System.out.println(day);
        }
    }
}
