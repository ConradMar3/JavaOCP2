package Book02_ProgrammingBasics.Chapter02.Strings;

public class DeclaringAndInitializingStrings {
    public static void main(String[] args) {
        String s;
        s = "Hello World!";

        //Other method:
        String t = "Java.";

        //Initializing an empty local string variable:
        String v = " ";

        System.out.println(s + " " + t + " " + v);
    }
}
